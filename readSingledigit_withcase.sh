read -p " Enter number [0-9]: " Number

case $Number in

0)
			echo "Zero"
;;

1)
         echo "ONE"
;;

2)
         echo "TWO"
;;

3)
         echo "THREE"
;;

4)
         echo "FOUR"
;;

5)
         echo "FIVE"
;;

6)
         echo "SIX"
;;

7)
         echo "SEVEN"
;;

8)
         echo "EIGHT"
;;

9)
         echo "NINE"
;;

*)
			echo "Not a single Number"
;;

esac;

